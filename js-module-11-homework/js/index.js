const refs = {
  form: document.querySelector(".js-form"),
  resetButton: document.querySelector(".js-reset"),
  source: document.querySelector("#root")
};

const filter = {};

// ====================================

refs.form.addEventListener("submit", handleFormSubmit);
refs.resetButton.addEventListener("click", handleResetButtonClick);

// ====================================

function handleFormSubmit(event) {
  event.preventDefault();

  const input = {
    size: document.querySelector('input[name="size"]:checked'),
    color: document.querySelector('input[name="color"]:checked'),
    release_date: document.querySelector('input[name="release_date"]:checked')
  };

  filter.size = input.size.value;
  filter.color = input.color.value;
  filter.release_date = input.release_date.value;

  getMarkup(filter);
}

function getMarkup(obj) {
  const userChoiceObj = laptops.find(
    element =>
      element.size == obj.size &&
      element.color === obj.color &&
      element.release_date == obj.release_date
  );

  if (userChoiceObj === undefined) {
    alert("Not Found!");
  } else {
    const cardTemplate = document.querySelector("#card").innerHTML.trim();
    const fnHandlebars = Handlebars.compile(cardTemplate);
    const markup = fnHandlebars(userChoiceObj);

    refs.source.innerHTML = markup;
  }
}

function handleResetButtonClick() {
  refs.source.innerHTML = "";
}
