const form = document.querySelector("form");
const boxes = document.querySelector(".boxes");
const submit = document.querySelector("button");
const inputs = document.querySelectorAll("input");

submit.addEventListener("click", setFiltered);

function setFiltered(e) {
  e.preventDefault();

  const props = [...inputs] // делаем массив инпутов из NodeList
    .filter(e => e.checked) // выбираем из него чекнутые инпуты
    .map(e => (e.name === "color" ? e.value : +e.value)); // выбираем в массив из инпутов значения полей и по необходимости преобразовываем их тип

  const selected = laptops
    .filter(e => props.includes(e.size))
    .filter(e => props.includes(e.color))
    .filter(e => props.includes(e.release_date));

  if (selected.length === 0) {
    show(laptops);
    return;
  }

  show(selected);
}

function show(arr) {
  const template = document.querySelector("#card").innerHTML.trim();
  const fnHandlebars = Handlebars.compile(template);
  const markup = arr.reduce((acc, element) => acc + fnHandlebars(element), "");
  boxes.innerHTML = markup;
}

show(laptops);
